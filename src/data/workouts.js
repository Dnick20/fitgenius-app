// Helper functions for workout management
export const getWorkoutsByCategory = (category) => {
  return WORKOUT_DATABASE[category] || [];
};

export const getAllWorkouts = () => {
  return Object.values(WORKOUT_DATABASE).flat();
};

export const getWorkoutById = (id) => {
  const allWorkouts = getAllWorkouts();
  return allWorkouts.find(workout => workout.id === id);
};

export const getWorkoutCategories = () => {
  return Object.keys(WORKOUT_DATABASE);
};

export const getRandomWorkouts = (count = 5) => {
  const allWorkouts = getAllWorkouts();
  const shuffled = allWorkouts.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const filterWorkoutsByDifficulty = (difficulty) => {
  const allWorkouts = getAllWorkouts();
  return allWorkouts.filter(workout => workout.difficulty === difficulty);
};

export const filterWorkoutsByDuration = (minDuration, maxDuration) => {
  const allWorkouts = getAllWorkouts();
  return allWorkouts.filter(workout => 
    workout.duration >= minDuration && workout.duration <= maxDuration
  );
};